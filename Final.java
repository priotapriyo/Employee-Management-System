    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        // attach file for employee
        
        JFileChooser chooser=new JFileChooser();      //file chooser for image attaching
        chooser.showOpenDialog(null);        //show null for not attaching photo
        File f=chooser.getSelectedFile();     //file selection path
        filename=f.getAbsolutePath();         //getting path
        try{
            File Image=new File(filename);        
            FileInputStream fis=new FileInputStream(Image);        //input image
            ByteArrayOutputStream bos=new ByteArrayOutputStream();    //storing in byte array
            byte[] buf=new byte[1024];                                //byte size decleration
            for(int readNum; (readNum=fis.read(buf))!=-1;){
               bos.write(buf,0,readNum);
            }
            person_image=bos.toByteArray();                 //variable set for setting array  
            Image img = Toolkit.getDefaultToolkit().createImage(person_image);
            ImageIcon icon = new ImageIcon(img);            //icon from desktop
            
           image.setIcon(icon);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);            //null pointer exception
        }
            
            
         
    }//GEN-LAST:event_jButton7ActionPerformed

