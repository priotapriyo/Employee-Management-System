    public void CurrentDate(){
        Thread clock;                 //thread for time
        clock = new Thread(){
            public void run(){
                for(;;){
                    Calendar cal=new GregorianCalendar();   //calendar used for current date 
                    int month=cal.get(Calendar.MONTH);       //month
                    int year=cal.get(Calendar.YEAR);           //year
                    int day=cal.get(Calendar.DAY_OF_MONTH);     //current date
                    jMenu4.setText("Date"+day+"-"+(month+1)+"-"+year);     //jmenu4 for date
                    
                    int hour=cal.get(Calendar.HOUR);     
                    int min=cal.get(Calendar.MINUTE);
                    int sec=cal.get(Calendar.SECOND);
                    jMenu5.setText("Time"+hour+":"+(min)+":"+sec);    //jmenu5 for time
                    
                    try{
                        sleep(1000);        //thread control
                    }catch(InterruptedException ex){
                    //Logger.getLogger(Final.class.getName()).log(Level.SEVERE,null,ex);
                    }
                    
                    
                }
            }
        };
        clock.start();
    }

