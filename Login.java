    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
              /// LOGIN ///        
        String s1 = "select * from users where UserName=? and Password=?";
        try{
            pst=conn.prepareStatement(s1);                  //statements connected to database
            pst.setString(1,jTextField1.getText());         //USERNAME
            pst.setString(2,jPasswordField1.getText());     //PASSWORD
            rs = pst.executeQuery();                        // query execution
            if(rs.next())                                   //if it executes
            {
                rs.close();
                pst.close();
                
                setVisible(false);
                Final ob = new Final();                     //Final frame call
               // ob.setUploading();
                ob.setVisible(true);                        //final frame true
            }
            else{
                JOptionPane.showMessageDialog(null, "Invalid UserName or Password!!");
            }
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }finally{
            try{
                rs.close();                          //finally result and statement closing
                pst.close();
            }catch(Exception e)
            {
                
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

